/**
 * The different kinds of custom task inputs.
 */
export enum InputTypes {
    /**
     * A string type of input.
     */
    StringValue = 1,

    /**
     * A boolean type of input.
     */
    BoolValue = 2 ,

    /**
     * A delimited type of input.
     */
    DelimitedValue = 3,

    /**
     * Represents no input at all.
     */
    None = 4
}